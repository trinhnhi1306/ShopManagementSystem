    public void filter(String str) {
        TableRowSorter<TableModel> trs = new TableRowSorter<>(jTable_Order.getModel());
        jTable_Order.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str, 3));
    }

